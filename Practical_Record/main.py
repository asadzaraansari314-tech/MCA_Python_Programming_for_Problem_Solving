from shapes import circle_area, circle_perimeter
from shapes import rectangle_area, rectangle_perimeter

# Circle
r = 5
print("Circle Area:", circle_area(r))
print("Circle Perimeter:", circle_perimeter(r))

# Rectangle
l = 10
w = 4
print("\nRectangle Area:", rectangle_area(l, w))
print("Rectangle Perimeter:", rectangle_perimeter(l, w))
